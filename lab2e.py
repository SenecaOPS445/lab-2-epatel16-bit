# Initialize the timer variable to 10
timer = 10

# While loop to count down from 10
while timer > 0:
    print(timer)  # Print the current value of timer
    timer -= 1  # Decrease timer by 1 on each iteration

# Print "blast off!" once the timer reaches 0
print("blast off!")

